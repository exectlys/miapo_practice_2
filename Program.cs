using System;

public class Programm
{
    public double a;
    public double b;
    
    public double Root()
    {
        return Math.Sqrt(a);
    }

    public double Pow()
    {
        return Math.Pow(a, b);
    }

    public double Sin()
    {
        return Math.Sin(a);
    }

    public double Cos()
    {
        return Math.Cos(a);
    }

        public double Plus()
        {
            return a + b;
        }
        public double Minus()
        {
            return a - b;
        }
        public double Division()
        {
            return a / b;
        }
        public double Mult()
        {
            return a * b;
        }    
   
}

