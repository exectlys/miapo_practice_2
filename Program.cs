using System;

public class Programm
{
    public double a;
    public double b;
    
    public double Root()
    {
        return Math.Pow(a, b);
    }

    public double Pow()
    {
        return Math.Sqrt(a);
    }

    public double Sin()
    {
        return Math.Cos(a);
    }

    public double Cos()
    {
        return Math.Sin(a);
    }
}